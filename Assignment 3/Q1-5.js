// 1. Age Check

let age = 17;

if (age >= 18) {
    console.log("Adult");
}
else {
    console.log("Not an adult");
}


// 2. Number Sign

let num1 = 20;

if (num1 >= 0) {
    console.log("Positive");
}
else {
    console.log("Negative");
}


// 3. Even or Odd

let num2 = 21;

if (num2 % 2 == 0) {
    console.log("Even");
}
else {
    console.log("Odd");
}


// 4. Vowel or Consonant

let letter = 'I';

if (letter == 'a' || letter == 'e' || letter == 'i' || letter == 'o' || letter == 'u' || letter == 'A' || letter == 'E' || letter == 'I' || letter == 'O' || letter == 'U') {
    console.log("Vowel")
}
else {
    console.log("Consonant");
}


// 5. Check Zero

let num3 = 0;

if (num3 == 0) {
    console.log("Zero");
}
else {
    console.log("Non-zero");
}